var numero = 1;
switch (numero) {
    case 1:
        console.log("soy un uno")
        break;
    case 2:
        console.log("Soy un dos")
        break;
    case 3:
        console.log("Soy un tres")
    default:
        console.log("No soy ninguna de las opciones")
        //El break es importante para no seguir validando
        break;
}